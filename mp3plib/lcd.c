void LCD_Set_Address(uint32_t PX1, uint32_t PY1, uint32_t PX2, uint32_t PY2)
{
	/*
		68 = 0x0044 (HRADRPOS)	=> Horizontal RAM Address Position
		69 = 0x0045 (VRADSTPO)	=> Vertical RAM Address Start Position
		70 = 0x0046 (VRADEDPO)	=> Vertical RAM Address End Position
		78 = 0x004E (STGDRXAC)	=> Set GDDRAM X Address Counter
		79 = 0x004F (STGDRYAC)	=> Set GDDRAM Y Address Counter
		34 = 0x0022	(RAMDATRW)	=> RAM Data Read/Write
	*/
	WriteRegister(68,(PX2 << 8) + PX1 );  	//Column address start2
	WriteRegister(69,PY1);      			//Column address start1
	WriteRegister(70,PY2);  				//Column address end2
	WriteRegister(78,PX1);     				//Column address end1
	WriteRegister(79,PY1);  				//Row address start2
	WriteCommand(34);
}

void LCD_Fill(uint32_t color)
{
	uint32_t i,j;

	CS(ENABLE);
	  
	LCD_Set_Address(0,0,LCD_WIDTH-1,LCD_HEIGHT-1);
	WriteData(color);
	
	for(i = 0; i <= LCD_HEIGHT-1; i++)
	{
		for(j = 0; j <= LCD_WIDTH-1; j++)
		{
			WR(DISABLE);
			WR(ENABLE);
		}
	}

	CS(DISABLE);
}

void LCD_Box(uint32_t x1, uint32_t y1, uint32_t x2, uint32_t y2, uint32_t color)
{
	uint32_t  i,j;
	
	CS(ENABLE);
	
	LCD_Set_Address(x1,y1,x2,y2);
	WriteData(color);
	
	for(i = y1; i <= y2; i++)
	{
		for(j = x1; j <= x2; j++)
		{
			WR(DISABLE);
			WR(ENABLE);
		}
	}

	CS(DISABLE);
}

void LCD_Dot(uint32_t x, uint32_t y, uint32_t color)
{
	CS(ENABLE);
	
	LCD_Set_Address(x,y,x,y);
	WriteData(color);
	
	CS(DISABLE);
}

void LCD_Char(char C, uint32_t x, uint32_t y, char DimFont, uint32_t Fcolor, uint32_t Bcolor)
{
	const char *ptrfont;
	uint32_t Cptrfont;
	uint32_t font8x8[8];
	uint32_t font16x16[16];
	unsigned char k,i,print1,print2;
	uint32_t print3,print4;

	if(DimFont == 8)
	{
		ptrfont = (char *) &FONT_8x8;
		Cptrfont = (C-32)*8;
		ptrfont = ptrfont + Cptrfont;
		for(k = 0; k <= 7; k++)
		{
			font8x8[k] = *ptrfont;
			ptrfont++;
		}

		CS(ENABLE);
		 
		LCD_Set_Address(x,y,x+7,y+7);
		for(i = 0; i <= 7; i++)
		{
			for(k = 0; k <= 7; k++)
			{
				print1 = (font8x8[i] & 0x80);
				print2 = print1 >>7;
				if(print2 == 1)
				{
					WriteData(Fcolor);
				}
				else
				{
					WriteData(Bcolor);
				}
				font8x8[i] = font8x8[i] << 1;
			}
		}
		CS(DISABLE);
	}

	else if(DimFont == 16)
	{
		ptrfont = (char *) &FONT_16x16;
		Cptrfont = (C-32)*32;
		ptrfont = ptrfont + Cptrfont;

		for(k = 0; k <= 15; k++)
		{
			font16x16[k] = *ptrfont;
			ptrfont++;
			font16x16[k] = (font16x16[k] << 8);
			font16x16[k] = font16x16[k] + *ptrfont;
			ptrfont++;
		}

		CS(ENABLE);
		LCD_Set_Address(x,y,x+15,y+15);
		for(i = 0; i <= 15; i++)
		{
			for(k = 0; k <= 15; k++)
			{
				print3 = (font16x16[i] & 0x8000);
				print4 = print3 >>15;

				if(print4 == 1)
				{
					WriteData(Fcolor);
				}
				else
				{
					WriteData(Bcolor);
				}

				font16x16[i] = font16x16[i] << 1;
			}
		}
		CS(DISABLE);
	}
}

void LCD_Image(uint32_t pos_x, uint32_t pos_y, uint32_t dim_x, uint32_t dim_y, const uint32_t *picture)
{
    uint32_t x, y;

    CS(ENABLE);
	
    LCD_Set_Address(pos_x, pos_y, pos_x + dim_x - 1, pos_y + dim_y - 1);
    for(y = pos_y; y < (pos_y + dim_y); y++ ) {
        for(x = pos_x; x < (pos_x + dim_x); x++ ) {
			WriteData(*picture++);
		}
	}
	
	CS(DISABLE);
}

