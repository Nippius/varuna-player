void LCD_Line(uint32_t x1, uint32_t y1, uint32_t x2, uint32_t y2, uint32_t color)
{
	int x,y,addx,addy,dx,dy;
	long P;
	int i;

	dx = fabs(x2-x1);
	dy = fabs(y2-y1);
	x = x1;
	y = y1;
	if(x1 > x2)
	{
		addx = -1;
	}
	else
	{
		addx = 1;
	}

	if(y1 > y2)
	{
		addy = -1;
	}
	else
	{
		addy = 1;
	}

	if(dx >= dy)
	{
		P = (2*dy) - dx;
		for(i = 1; i <= (dx +1); i++)
		{
			LCD_Dot(x,y,color);
			if(P < 0)
			{
				P = P + (2*dy);
				x = (x + addx);
			}
			else
			{
				P = P+(2*dy) - (2*dx);
				x = x + addx;
				y = y + addy;
			}
		}
	}
	else
	{
		P = (2*dx) - dy;
		for(i = 1; i <= (dy +1); i++)
		{
			LCD_Dot(x,y,color);
			if(P<0)
			{
				P = P + (2*dx);
				y = y + addy;
			}
			else
			{
				P = P + (2*dx) - (2*dy);
				x = x + addx;
				y = y + addy;
			}
		}
	}
}

